    while option != 'q'
      intro
      option = gets.chomp.downcase
      clear
      case option 
      when '1'
        puts "Insert the name of a Pokemon."
        Search.search_pokemon
      when '2'
        puts "Insert the name of a Type."
        Search.search_type
      when '3'
        puts "Insert the name of an Ability."
        Search.search_ability
      when 'q'
        shut_down
      else
        invalid_input
      end
    end
  end

